async function handleDeveloperAction(req, res, session, action) {
  if (!isDeveloperSession(session)) {
    return sendHtml(res, accessDeniedPage(), 403);
  }

  const { user, companies, access } = getDeveloperPageContext(session);

  if (!user || user.status !== 'active' || !access) {
    return sendHtml(res, accessDeniedPage(), 403);
  }

  const form = await readBody(req);

  try {
    if (action === 'create-company') {
      const company = {
        company_name: String(form.get('company_name') || '').trim(),
        website_url: String(form.get('website_url') || '').trim(),
        logo_url: String(form.get('logo_url') || '').trim(),
        industry: String(form.get('industry') || '').trim(),
        service_area: String(form.get('service_area') || '').trim(),
        target_country: String(form.get('target_country') || '').trim(),
        main_services: String(form.get('main_services') || '').trim(),
        known_competitors: '',
        brand_description: '',
        target_audience: String(form.get('target_audience') || '').trim(),
        status: COMPANY_STATUSES.includes(String(form.get('status'))) ? String(form.get('status')) : 'active'
      };

      if (!company.company_name || !company.website_url) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Company name and website URL are required.' }), 422);
      }

      createCompanyForDeveloper(company);
      session.workspaceCompanies = getWorkspaceCompaniesForSession(session);
      return redirect(res, '/developer?success=company-created', 303);
    }

    if (action === 'company-status') {
      const companyId = Number(form.get('company_id'));
      const status = String(form.get('status') || '').trim();

      if (!Number.isInteger(companyId) || !COMPANY_STATUSES.includes(status)) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Invalid company status request.' }), 422);
      }

      setCompanyStatus(companyId, status);
      session.workspaceCompanies = getWorkspaceCompaniesForSession(session);
      return redirect(res, '/developer?success=company-updated', 303);
    }

    if (action === 'company-delete') {
      const companyId = Number(form.get('company_id'));

      if (!Number.isInteger(companyId) || companyId <= 0) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Invalid company delete request.' }), 422);
      }

      deleteCompany(companyId);
      session.workspaceCompanies = getWorkspaceCompaniesForSession(session);

      if (session.selectedCompanyId === companyId) {
        const nextCompany = session.workspaceCompanies[0];

        if (nextCompany) {
          const nextAccess = getDeveloperCompanyAccess(nextCompany.company_id);
          Object.assign(session, snapshotAccess(nextAccess), snapshotProfile(nextAccess));
        } else {
          Object.assign(session, {
            selectedCompanyId: null,
            selectedCompanyName: 'Platform Admin',
            selectedRoleId: null,
            selectedRoleName: 'Developer'
          });
        }
      }

      return redirect(res, '/developer?success=company-deleted', 303);
    }

    if (action === 'user-status') {
      const userId = Number(form.get('user_id'));
      const status = String(form.get('status') || '').trim();

      if (!Number.isInteger(userId) || !COMPANY_ACCESS_STATUSES.includes(status)) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Invalid user status request.' }), 422);
      }

      setUserStatus(userId, status);
      return redirect(res, '/developer?success=user-updated', 303);
    }

    if (action === 'access-add') {
      const userId = Number(form.get('user_id'));
      const companyId = Number(form.get('company_id'));
      const roleName = String(form.get('role_name') || '').trim();
      const status = String(form.get('status') || '').trim();

      if (!Number.isInteger(userId) || !Number.isInteger(companyId) || !getAssignableUserRoles('Developer').includes(roleName) || !COMPANY_ACCESS_STATUSES.includes(status)) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Invalid access add request.' }), 422);
      }

      addExistingUserCompanyAccess({ userId, companyId, roleName, status });
      return redirect(res, '/developer?success=access-added', 303);
    }

    if (action === 'access-update') {
      const accessId = Number(form.get('access_id'));
      const roleName = String(form.get('role_name') || '').trim();
      const status = String(form.get('status') || '').trim();

      if (!Number.isInteger(accessId) || !getAssignableUserRoles('Developer').includes(roleName) || !COMPANY_ACCESS_STATUSES.includes(status)) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Invalid access update request.' }), 422);
      }

      updateAccessRecordById({ accessId, roleName, status });
      return redirect(res, '/developer?success=access-updated', 303);
    }

    if (action === 'access-remove') {
      const accessId = Number(form.get('access_id'));
      const record = Number.isInteger(accessId) ? getAccessRecordById(accessId) : null;

      if (!record) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Access record was not found.' }), 404);
      }

      if (record.role_name === 'Business Owner' && countCompanyBusinessOwners(record.company_id) <= 1) {
        return sendHtml(res, developerPage({ user, access, companies, message: 'Cannot remove the last Business Owner from this company.' }), 422);
      }

      removeAccessRecordById(accessId);
      return redirect(res, '/developer?success=access-removed', 303);
    }
  } catch (error) {
    if (error.message === 'DUPLICATE_COMPANY_ACCESS' || String(error.message || '').includes('UNIQUE')) {
      return sendHtml(res, developerPage({ user, access, companies, message: 'User already has access to this company.' }), 409);
    }

    throw error;
  }

  return sendHtml(res, accessDeniedPage(), 403);
}

