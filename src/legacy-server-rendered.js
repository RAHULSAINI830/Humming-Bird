function hasValue(value) {
  return Boolean(String(value || '').trim());
}

function valueOrFallback(value, fallback = 'Not added yet') {
  return hasValue(value) ? String(value).trim() : fallback;
}

function getFieldStatus(access, fields) {
  const completed = fields.filter(([fieldName]) => hasValue(access[fieldName]));
  const missing = fields.filter(([fieldName]) => !hasValue(access[fieldName]));

  return {
    completed,
    missing,
    percentage: Math.round((completed.length / fields.length) * 100)
  };
}

