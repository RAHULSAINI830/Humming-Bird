function updateCachedCompany(session, access) {
  if (!Array.isArray(session.workspaceCompanies)) {
    return;
  }

  session.workspaceCompanies = session.workspaceCompanies.map((company) => {
    if (company.company_id !== access.company_id) {
      return company;
    }

    return {
      ...company,
      company_name: access.company_name,
      website_url: access.website_url,
      logo_url: access.logo_url,
      role_id: access.role_id,
      role_name: access.role_name
    };
  });
}

