    if (!user || user.status !== 'active') {
      destroySession(req, res);
      return redirect(res, '/login');
    }

    if (!session.selectedCompanyId) {
      return redirect(res, '/workspace');
    }

    const access = getSelectedAccessForSession(session);

    if (!access) {
      return sendHtml(res, accessDeniedPage(), 403);
    }

