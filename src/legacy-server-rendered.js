function validateCompanyUserInput(input, currentRoleName) {
  const errors = {};
  const allowedRoles = getAssignableUserRoles(currentRoleName);

  if (!input.full_name) errors.full_name = 'Full name is required.';
  if (!input.email) {
    errors.email = 'Email is required.';
  } else if (!isValidEmail(input.email)) {
    errors.email = 'Enter a valid email address.';
  }
  if (!input.password) errors.password = 'Password is required for new users.';
  if (input.confirm_password !== input.password) {
    errors.confirm_password = 'Confirm password must match password.';
  }
  if (!allowedRoles.includes(input.role_name)) {
    errors.role_name = 'Select an allowed role.';
  }
  if (!COMPANY_ACCESS_STATUSES.includes(input.status)) {
    errors.status = 'Select a valid status.';
  }

  return errors;
}

