function sendHtml(res, html, statusCode = 200) {
  res.writeHead(statusCode, {
    'Content-Type': 'text/html; charset=utf-8',
    'Cache-Control': 'no-store, no-cache, must-revalidate, proxy-revalidate',
    Pragma: 'no-cache',
    Expires: '0'
  });
  res.end(html);
}

function sendJson(res, payload, statusCode = 200) {
  res.writeHead(statusCode, {
    'Content-Type': 'application/json; charset=utf-8',
    'Cache-Control': 'no-store, no-cache, must-revalidate, proxy-revalidate',
    Pragma: 'no-cache',
    Expires: '0'
  });
  res.end(JSON.stringify(payload));
}

function staticContentType(filePath) {
  const ext = path.extname(filePath).toLowerCase();

  if (ext === '.html') return 'text/html; charset=utf-8';
  if (ext === '.js' || ext === '.mjs') return 'text/javascript; charset=utf-8';
  if (ext === '.css') return 'text/css; charset=utf-8';
  if (ext === '.json') return 'application/json; charset=utf-8';
  if (ext === '.svg') return 'image/svg+xml';
  if (ext === '.png') return 'image/png';
  if (ext === '.jpg' || ext === '.jpeg') return 'image/jpeg';
  if (ext === '.webp') return 'image/webp';
  if (ext === '.ico') return 'image/x-icon';
  if (ext === '.woff') return 'font/woff';
  if (ext === '.woff2') return 'font/woff2';

  return 'application/octet-stream';
}

function serveBuiltReactFrontend(req, res, url) {
  if (!fs.existsSync(frontendDistPath)) {
    return sendHtml(res, layout({
      title: 'React frontend not built',
      body: `
        <section class="content-card">
          <p class="eyebrow">React frontend</p>
          <h1>React frontend is not built yet</h1>
          <p class="muted">Run <code>npm --prefix frontend install</code> and then <code>npm run frontend:build</code>. For local React development, run <code>npm run frontend:dev</code>.</p>
          <a class="button-link" href="/dashboard">Back to server dashboard</a>
        </section>
      `
    }), 503);
  }

  const relativePath = decodeURIComponent(url.pathname.replace(/^\/app\/?/, ''));
  const requestedPath = relativePath ? path.join(frontendDistPath, relativePath) : path.join(frontendDistPath, 'index.html');
  const normalizedPath = path.normalize(requestedPath);
  const safePath = normalizedPath.startsWith(frontendDistPath) && fs.existsSync(normalizedPath) && fs.statSync(normalizedPath).isFile()
    ? normalizedPath
    : path.join(frontendDistPath, 'index.html');

  res.writeHead(200, {
    'Content-Type': staticContentType(safePath),
    'Cache-Control': safePath.endsWith('index.html') ? 'no-store' : 'public, max-age=31536000, immutable'
  });
  return res.end(fs.readFileSync(safePath));
}

