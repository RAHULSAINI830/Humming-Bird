function validateSignupInput(input) {
  const errors = {};

  if (!input.full_name) errors.full_name = 'Full name is required.';
  if (!input.email) {
    errors.email = 'Email is required.';
  } else if (!isValidEmail(input.email)) {
    errors.email = 'Enter a valid email address.';
  }
  if (!input.password) errors.password = 'Password is required.';
  if (input.confirm_password !== input.password) {
    errors.confirm_password = 'Confirm password must match password.';
  }
  if (!input.company_name) errors.company_name = 'Company name is required.';
  if (!input.website_url) errors.website_url = 'Website URL is required.';

  return errors;
}

