function safeJsonArray(value) {
  if (!value) return [];

  try {
    const parsed = JSON.parse(value);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

function promptVisibilitySummary(prompts) {
  const checked = prompts.filter((prompt) => prompt.visibility_status && prompt.visibility_status !== 'not_checked');
  const brandMentioned = prompts.filter((prompt) => Number(prompt.brand_mentioned || 0) === 1);
  const competitorMentionCount = prompts.reduce((total, prompt) => total + safeJsonArray(prompt.competitor_mentions).length, 0);

  return {
    checked: checked.length,
    brandMentioned: brandMentioned.length,
    competitorMentionCount
  };
}

function faviconUrlFor(value) {
  const rawValue = String(value || '').trim();

  if (!rawValue || rawValue === 'Unknown') {
    return '';
  }

  const normalized = /^https?:\/\//i.test(rawValue) ? rawValue : `https://${rawValue}`;
  return `https://www.google.com/s2/favicons?domain_url=${encodeURIComponent(normalized)}&sz=32`;
}

function competitorMentionChips(prompt) {
  const mentions = safeJsonArray(prompt.competitor_mentions);

  if (!mentions.length) {
    return '<span class="muted">None</span>';
  }

  return `
    <div class="favicon-chip-row">
      ${mentions.map((competitor) => {
        const faviconUrl = faviconUrlFor(competitor.website_url);
        return `
          <span class="favicon-chip" title="${escapeHtml(competitor.mention_context || competitor.competitor_name || '')}">
            ${faviconUrl ? `<img src="${escapeHtml(faviconUrl)}" alt="" loading="lazy" />` : '<span class="favicon-placeholder">•</span>'}
            ${escapeHtml(competitor.competitor_name || 'Competitor')}
          </span>
        `;
      }).join('')}
    </div>
  `;
}

