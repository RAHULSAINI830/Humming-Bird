function safeAnalysisError(error) {
  if (error?.message === 'AI_MISSING_KEY') return 'Gemini API key is not configured.';
  if (error?.message === 'AI_INVALID_JSON') return 'AI response could not be parsed.';
  if (error?.message === 'AI_TIMEOUT') return 'AI request timed out.';
  if (error?.message === 'AI_AUTH_FAILED') return 'Gemini authentication failed. Please check GEMINI_API_KEY.';
  if (error?.message === 'AI_RATE_LIMITED') return 'Gemini free tier rate limit or quota was reached. Please try again later.';
  if (error?.message === 'AI_SERVER_ERROR') return 'Gemini service returned an error. Please try again later.';
  if (error?.message === 'AI_NETWORK_ERROR') return 'Could not connect to Gemini. Please check the network connection.';
  if (error?.message === 'AI_REQUEST_FAILED') return 'Gemini rejected the analysis request. Please check the API key, model, and request configuration.';
  return 'Business analysis failed. Please retry.';
}

