async function handleAddManualPrompt(req, res, session, user, access) {
  if (!canGeneratePrompts(access.role_name)) {
    return sendHtml(res, accessDeniedPage(), 403);
  }

  const form = await readBody(req);
  const promptText = String(form.get('prompt_text') || '').trim();
  const promptCategory = String(form.get('prompt_category') || 'Manual').trim();
  const promptIntent = String(form.get('prompt_intent') || 'Manual tracking').trim();

  if (!promptText) {
    return sendHtml(res, promptsPage({
      user,
      access,
      companies: session.workspaceCompanies || getWorkspaceCompaniesForSession(session),
      prompts: listCompanyPrompts(access.company_id),
      competitors: listCompanyCompetitors(access.company_id),
      analysis: getLatestCompletedBusinessAnalysis(access.company_id) || getLatestBusinessAnalysis(access.company_id),
      message: 'Prompt text is required.'
    }), 422);
  }

  const result = addCompanyPrompt({
    companyId: access.company_id,
    promptText,
    promptCategory,
    promptIntent
  });

  const latestAnalysis = getLatestCompletedBusinessAnalysis(access.company_id);

  if (!latestAnalysis) {
    return redirect(res, '/prompts?success=prompt-added&error=analysis-required', 303);
  }

  try {
    let competitors = listCompanyCompetitors(access.company_id);

    if (!competitors.length) {
      const discovered = await AIService.discoverCompetitors(access, latestAnalysis);
      upsertCompanyCompetitors(access.company_id, discovered, 'gemini');
      competitors = listCompanyCompetitors(access.company_id);
    }

    const prompt = getCompanyPromptById(access.company_id, Number(result.lastInsertRowid));

    if (prompt) {
      const results = await AIService.analyzePromptVisibility(access, [prompt], competitors, latestAnalysis);
      updatePromptVisibility(access.company_id, results);
    }

    return redirect(res, '/prompts?success=prompt-added-checked', 303);
  } catch (error) {
    return redirect(res, '/prompts?success=prompt-added&error=check-failed', 303);
  }
}

