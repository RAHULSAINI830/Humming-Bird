function fieldControl(field, access, errors, editable) {
  const value = escapeHtml(access[field.name] || '');
  const describedBy = `${field.name}_helper`;
  const error = errors[field.name];
  const onboardingRequired = ONBOARDING_REQUIRED_FIELDS.some(([fieldName]) => fieldName === field.name);
  const commonAttributes = `
    name="${field.name}"
    id="${field.name}"
    aria-describedby="${describedBy}"
    ${field.required ? 'required' : ''}
    ${editable ? '' : 'disabled'}
  `;

  return `
    <label class="form-field ${field.multiline ? 'span-two' : ''}" for="${field.name}">
      <span>${escapeHtml(field.label)}${onboardingRequired ? ' <em>Onboarding</em>' : ''}${field.generated ? ' <em>AI-generated</em>' : ''}</span>
      ${field.multiline
        ? `<textarea ${commonAttributes} rows="4">${value}</textarea>`
        : `<input ${commonAttributes} value="${value}" />`
      }
      <small id="${describedBy}">${escapeHtml(field.helper)}</small>
      ${error ? `<strong class="field-error">${escapeHtml(error)}</strong>` : ''}
    </label>
  `;
}

