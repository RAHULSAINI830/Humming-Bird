function businessAnalysisCardTitle(analysis) {
  if (!analysis) return 'No business analysis yet';
  if (analysis.analysis_status === 'in_progress') return 'In progress';
  if (analysis.analysis_status === 'completed') return 'Completed';
  if (analysis.analysis_status === 'failed') return 'Failed';
  return 'No business analysis yet';
}

function businessAnalysisCardText(analysis) {
  if (!analysis) return 'No analysis yet.';
  if (analysis.analysis_status === 'completed') return analysis.business_summary || 'Completed analysis is saved.';
  if (analysis.analysis_status === 'in_progress') return 'Business analysis is running.';
  if (analysis.analysis_status === 'failed') return analysis.error_message || 'Business analysis failed.';
  return 'No real business analysis has been generated yet.';
}

function statusBadgeClass(status) {
  if (status === 'completed') return 'complete';
  if (status === 'failed') return 'danger';
  if (status === 'in_progress') return 'warning';
  return '';
}

