async function handleUpdateCompanyUser(req, res, session, user, access) {
  if (!canManageCompanyUsers(access.role_name)) {
    return sendHtml(res, accessDeniedPage(), 403);
  }

  const form = await readBody(req);
  const input = normalizeCompanyUserEditInput(form);
  const errors = validateCompanyUserEditInput(input, access.role_name);
  const existingAccess = Number.isInteger(input.user_id)
    ? getCompanyUserAccess(access.company_id, input.user_id)
    : null;

  if (!existingAccess) {
    errors.user_id = 'User does not have access to this company.';
  }

  if (
    existingAccess &&
    existingAccess.role_name === 'Business Owner' &&
    (input.role_name !== 'Business Owner' || input.status !== 'active') &&
    countCompanyBusinessOwners(access.company_id) <= 1
  ) {
    errors.role_name = 'Cannot remove the last Business Owner from this company.';
  }

  if (Object.keys(errors).length > 0) {
    return sendHtml(res, usersPage({
      user,
      access,
      companies: session.workspaceCompanies || getWorkspaceCompaniesForSession(session),
      companyUsers: listCompanyUsers(access.company_id),
      errors,
      message: Object.values(errors)[0]
    }), 422);
  }

  updateCompanyUserAccess({
    companyId: access.company_id,
    userId: input.user_id,
    roleName: input.role_name,
    status: input.status
  });

  if (input.user_id === session.userId) {
    const refreshedAccess = getUserCompanyAccess(session.userId, access.company_id);

    if (refreshedAccess) {
      Object.assign(session, snapshotAccess(refreshedAccess), snapshotProfile(refreshedAccess));
    }
  }

  return redirect(res, '/users?success=user-updated', 303);
}

