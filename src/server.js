async function handleLogin(req, res) {
  const form = await readBody(req);
  const email = String(form.get('email') || '').trim();
  const password = String(form.get('password') || '');
  const user = getUserByEmail(email);

  if (!user || !verifyPassword(password, user.password_hash)) {
    return sendHtml(res, loginPage('Invalid email or password.'), 401);
  }

  if (user.status !== 'active') {
    return sendHtml(res, loginPage('Your account is not active. Please contact an administrator.'), 403);
  }

  const isDeveloper = userHasRole(user.id, 'Developer');
  const companies = isDeveloper ? getAllCompaniesForWorkspace() : getUserCompanies(user.id);

  if (companies.length === 0 && !isDeveloper) {
    return sendHtml(res, loginPage('No active company access was found for this account.'), 403);
  }

  const session = createSession(res, {
    userId: user.id,
    userFullName: user.full_name,
    userEmail: user.email,
    userStatus: user.status,
    isDeveloper,
    workspaceCompanies: companies,
    selectedCompanyId: null,
    selectedCompanyName: null,
    selectedRoleId: null,
    selectedRoleName: null
  });

  if (companies.length === 0 && isDeveloper) {
    Object.assign(session, {
      selectedCompanyId: null,
      selectedCompanyName: 'Platform Admin',
      selectedRoleId: null,
      selectedRoleName: 'Developer'
    });
    return redirect(res, '/developer');
  }

  if (companies.length === 1) {
    const access = isDeveloper
      ? getDeveloperCompanyAccess(companies[0].company_id)
      : getUserCompanyAccess(user.id, companies[0].company_id);
    Object.assign(session, snapshotAccess(access), snapshotProfile(access));
    return redirect(res, '/dashboard');
  }

  return redirect(res, '/workspace');
}

async function handleSignup(req, res) {
  const form = await readBody(req);
  const input = normalizeSignupInput(form);
  const errors = validateSignupInput(input);

  if (Object.keys(errors).length > 0) {
    return sendHtml(res, signupPage({ input, errors }), 422);
  }

  if (getUserByEmail(input.email)) {
    return sendHtml(res, signupPage({
      input,
      errors: { email: 'An account already exists for this email.' },
      message: 'An account already exists for this email. Try logging in instead.'
    }), 409);
  }

  let created;

  try {
    created = createUserCompanyWorkspace({
      user: {
        full_name: input.full_name,
        email: input.email,
        password_hash: hashPassword(input.password)
      },
      company: {
        company_name: input.company_name,
        website_url: input.website_url,
        logo_url: input.logo_url,
        industry: '',
        service_area: '',
        target_country: '',
        main_services: '',
        known_competitors: '',
        brand_description: '',
        target_audience: ''
      }
    });
  } catch (error) {
    if (String(error.message || '').includes('UNIQUE')) {
      return sendHtml(res, signupPage({
        input,
        errors: { email: 'An account already exists for this email.' },
        message: 'An account already exists for this email. Try logging in instead.'
      }), 409);
    }

    throw error;
  }

  const user = getUserById(created.userId);
  const companies = getUserCompanies(created.userId);
  const access = getUserCompanyAccess(created.userId, created.companyId);
  const session = createSession(res, {
    userId: user.id,
    userFullName: user.full_name,
    userEmail: user.email,
    userStatus: user.status,
    isDeveloper: false,
    workspaceCompanies: companies,
    ...snapshotAccess(access),
    ...snapshotProfile(access)
  });

  Object.assign(session, snapshotAccess(access), snapshotProfile(access));

  return redirect(res, '/dashboard', 303);
}

