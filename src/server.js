  const websiteUrl = String(form.get('website_url') || '').trim();
  const notes = String(form.get('notes') || '').trim();

  if (!competitorName) {
    return redirect(res, `${redirectPath}?error=competitor-required`, 303);
  }

  addCompanyCompetitor({
    companyId: access.company_id,
    competitorName,
    websiteUrl,
    notes
  });

  const latestAnalysis = getLatestCompletedBusinessAnalysis(access.company_id);
  const prompts = listCompanyPrompts(access.company_id);

